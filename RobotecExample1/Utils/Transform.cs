public class Transform
{
    public float X;
    public float Y;
    public float Z;
    public float A;
    public float B;
    public float C;

    public Transform(float x, float y, float z, float a, float b, float c)
    {
        X = x;
        Y = y;
        Z = z;
        A = a;
        B = b;
        C = c;
    }
}